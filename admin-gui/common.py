# PBKDF2 helpers and admin credential management

def mosq_pbkdf2_sha512_hash(password: str, iterations: int = 20000, salt_bytes: int = 12) -> str:
    """Return a Mosquitto-compatible PBKDF2-SHA512 hash in $7$ format."""
    if isinstance(password, str):
        password = password.encode("utf-8")
    salt = os.urandom(salt_bytes)
    dk = hashlib.pbkdf2_hmac("sha512", password, salt, iterations)
    b64salt = base64.b64encode(salt).decode("ascii")
    b64dk = base64.b64encode(dk).decode("ascii")
    return f"$7${iterations}${b64salt}${b64dk}"


def verify_mosq_7(password: str, stored: str) -> bool:
    """Verify a plaintext password against a stored $7$ PBKDF2-SHA512 hash."""
    try:
        if not stored.startswith("$7$"):
            return False
        _, iters, b64salt, b64dk = stored.split("$")[1:5]
        iters = int(iters)
        salt = base64.b64decode(b64salt)
        want = base64.b64decode(b64dk)
        got = hashlib.pbkdf2_hmac("sha512", password.encode("utf-8"), salt, iters)
        return hmac.compare_digest(got, want)
    except Exception:
        return False


def load_admin_credentials() -> Tuple[Optional[str], Optional[Callable[[str], bool]]]:
    """Load the admin username and a password verification callable."""
    if not os.path.exists(ADMIN_PWFILE):
        return None, None
    try:
        with open(ADMIN_PWFILE, "r", encoding="utf-8") as f:
            for line in f:
                line = line.strip()
                if not line or ":" not in line:
                    continue
                user, hashv = line.split(":", 1)
                user = user.strip()
                hashv = hashv.strip()
                if user and hashv:
                    return user, (lambda pw: verify_mosq_7(pw, hashv))
    except Exception:
        pass
    return None, None


def save_admin_credentials(username: str, password: str) -> None:
    """Persist the admin credentials to the ADMIN_PWFILE."""
    h = mosq_pbkdf2_sha512_hash(password, iterations=20000)
    os.makedirs(os.path.dirname(ADMIN_PWFILE), exist_ok=True)
    with open(ADMIN_PWFILE, "w", encoding="utf-8", newline="\n") as f:
        f.write(f"{username}:{h}\n")


def require_login() -> bool:
    """Enforce that the user is authenticated before continuing."""
    admin_user, admin_verify = load_admin_credentials()

    # Initialize session variables
    st.session_state.setdefault("auth_ok", False)
    st.session_state.setdefault("auth_user", None)

    # Bootstrap: if no admin credentials exist, prompt for them
    if admin_user is None or admin_verify is None:
        with st.container(border=True):
            st.subheader("Set Admin Credentials")
            col1, col2 = st.columns(2)
            with col1:
                username = st.text_input("Username", value="admin", key="init_username")
            with col2:
                password = st.text_input("New Password", type="password", key="init_password", help="Min 8 characters")
            if st.button("Save Admin Credentials", key="init_save", disabled=len(password) < 8):
                if not username or len(password) < 8:
                    st.error("Username required and password must be at least 8 characters.")
                else:
                    save_admin_credentials(username, password)
                    st.success("Admin credentials saved. Please log in.")
                    st.rerun()
        return False

    # Already logged in?
    if st.session_state.get("auth_ok") and st.session_state.get("auth_user") == admin_user:
        return True

    # Show login form
    with st.container(border=True):
        st.subheader("Sign in")
        u = st.text_input("Username", key="login_username")
        p = st.text_input("Password", type="password", key="login_password")
        if st.button("Sign in", key="login_submit"):
            if hmac.compare_digest(u or "", admin_user) and admin_verify(p or ""):
                st.session_state["auth_ok"] = True
                st.session_state["auth_user"] = admin_user
                st.rerun()
            else:
                st.error("Invalid username or password.")
    return False

