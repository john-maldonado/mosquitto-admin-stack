@dataclass
class PasswordFileEntry:
    """Metadata about a Mosquitto password file."""

    path: str
    label: str
    is_default: bool
    usernames: List[str]
    exists: bool
    size_bytes: Optional[int]
    modified: Optional[datetime]

    @property
    def user_count(self) -> int:
        return len(self.usernames)

