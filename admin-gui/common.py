"""
Shared utilities and authentication helper functions for the Mosquitto
Streamlit admin GUI. This module centralizes configuration paths,
password hashing, admin credential management, user management,
certificate generation, configuration file handling, and broker
reload logic. Each Streamlit page imports this module to gain
access to common functionality and a `require_login` function
that enforces file-based admin authentication.

The admin credentials are stored in `/config/admin_pwfile` in
Mosquitto's `$7$` PBKDF2-SHA512 format. The first time you run
the GUI, `require_login()` will prompt you to set an admin
username and password. Thereafter, pages will display a login
form until you authenticate.
"""

