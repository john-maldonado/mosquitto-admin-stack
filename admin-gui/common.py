# mosquitto.conf parsing (listeners with effective Auth/TLS)

_BOOL_TRUE = {"true", "1", "yes", "on"}

def _to_bool(s: str) -> bool:
    return s.strip().lower() in _BOOL_TRUE

def parse_mosquitto_conf_listeners(path: str = MOSQCONF):
    """
    Parse mosquitto.conf to extract listeners and their effective Auth/TLS config.

    Returns:
        (listeners, meta) where:
          listeners: List[{
              "port": int,
              "address": Optional[str],
              "auth": {
                  "allow_anonymous": Optional[bool],
                  "password_file": Optional[str],
                  "acl_file": Optional[str],
              },
              "tls": {
                  "cafile": Optional[str],
                  "capath": Optional[str],
                  "certfile": Optional[str],
                  "keyfile": Optional[str],
                  "require_certificate": Optional[bool],
                  "use_identity_as_username": Optional[bool],
                  "crlfile": Optional[str],
                  "tls_version": Optional[str],
                  "ciphers": Optional[str],
              },
              "per_listener_settings": bool
          }]
          meta: {
              "per_listener_settings": bool,
              "globals": { "auth": {...}, "tls": {...} }
          }
    """
    text = read_text_or(path, "")
    per_listener = False
    globals_auth: Dict[str, Any] = {}
    globals_tls: Dict[str, Any] = {}

    listeners: List[Dict[str, Any]] = []
    cur: Optional[Dict[str, Any]] = None

    def _apply_auth(target: Dict[str, Any], key: str, args: List[str]):
        if not args:
            return
        if key == "allow_anonymous":
            target["allow_anonymous"] = _to_bool(args[0])
        elif key == "password_file":
            target["password_file"] = " ".join(args)
        elif key == "acl_file":
            target["acl_file"] = " ".join(args)

    def _apply_tls(target: Dict[str, Any], key: str, args: List[str]):
        if not args:
            return
        if key in ("require_certificate", "use_identity_as_username"):
            target[key] = _to_bool(args[0])
        else:
            target[key] = " ".join(args)

    for raw in text.splitlines():
        line = raw.split("#", 1)[0].strip()
        if not line:
            continue
        parts = line.split()
        key = parts[0].lower()
        args = parts[1:]

        if key == "per_listener_settings" and args:
            per_listener = _to_bool(args[0])
            continue

        if key == "listener" and args:
            if cur:
                listeners.append(cur)
            port = int(args[0])
            addr = args[1] if len(args) > 1 else None
            cur = {"port": port, "address": addr, "auth": {}, "tls": {}}
            continue

        if key in ("allow_anonymous", "password_file", "acl_file"):
            if per_listener and cur is not None:
                _apply_auth(cur["auth"], key, args)
            else:
                _apply_auth(globals_auth, key, args)
            continue

        if key in (
            "cafile", "capath", "certfile", "keyfile", "require_certificate",
            "use_identity_as_username", "crlfile", "tls_version", "ciphers"
        ):
            if per_listener and cur is not None:
                _apply_tls(cur["tls"], key, args)
            else:
                _apply_tls(globals_tls, key, args)
            continue

    if cur:
        listeners.append(cur)

    # Compose effective settings per listener
    for l in listeners:
        for k, v in globals_auth.items():
            l["auth"].setdefault(k, v)
        for k, v in globals_tls.items():
            l["tls"].setdefault(k, v)
        l["per_listener_settings"] = per_listener

    meta = {
        "per_listener_settings": per_listener,
        "globals": {"auth": globals_auth, "tls": globals_tls},
    }
    return listeners, meta