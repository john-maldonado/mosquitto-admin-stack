# Broker reload / restart

def reload_broker() -> Tuple[bool, str]:
    """
    Reload mosquitto configuration.

    Strategy:
      1) If the Mosquitto process is visible in this namespace, send SIGHUP.
      2) Otherwise (or on failure), create a trigger file at $SIGNALS_DIR/reload
         for the mosquitto watcher to handle.
    """
    # Attempt direct SIGHUP if possible
    try:
        res = subprocess.run(["pidof", "mosquitto"], capture_output=True, text=True)
        if res.returncode == 0 and res.stdout.strip():
            pid = int(res.stdout.strip().split()[0])
            os.kill(pid, signal.SIGHUP)
            return True, "Reload requested via SIGHUP (pid namespace shared)."
    except Exception:
        pass

    # Fallback: write a reload trigger file
    try:
        os.makedirs(SIGNALS_DIR, exist_ok=True)
        tmp = os.path.join(SIGNALS_DIR, "reload.tmp")
        with open(tmp, "w", encoding="utf-8", newline="\n") as f:
            f.write("")
        os.replace(tmp, os.path.join(SIGNALS_DIR, "reload"))
        return True, f"Reload requested via trigger file in {SIGNALS_DIR}."
    except Exception as e:
        return False, f"Reload not possible: {e}"

def restart_broker() -> Tuple[bool, str]:
    """Request mosquitto to restart (graceful SIGTERM handled by in-broker watcher)."""
    try:
        os.makedirs(SIGNALS_DIR, exist_ok=True)
        tmp = os.path.join(SIGNALS_DIR, "restart.tmp")
        with open(tmp, "w", encoding="utf-8", newline="\n") as f:
            f.write("")
        os.replace(tmp, os.path.join(SIGNALS_DIR, "restart"))
        return True, f"Restart requested via trigger file in {SIGNALS_DIR}."
    except Exception as e:
        return False, f"Restart not possible: {e}"

