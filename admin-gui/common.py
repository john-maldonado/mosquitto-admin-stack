# TLS certificate generation

def gen_server_ca_and_cert(cn: str, alt_hosts: List[str], days: int = 825) -> None:
    """Generate a self-signed CA and server certificate/key for TLS."""
    now = datetime.utcnow()
    # Generate CA key and certificate
    ca_key = rsa.generate_private_key(public_exponent=65537, key_size=2048)
    ca_name = x509.Name([x509.NameAttribute(NameOID.COMMON_NAME, u"Mosquitto Server CA")])
    ca_cert = (
        x509.CertificateBuilder()
        .subject_name(ca_name)
        .issuer_name(ca_name)
        .public_key(ca_key.public_key())
        .serial_number(x509.random_serial_number())
        .not_valid_before(now - timedelta(minutes=1))
        .not_valid_after(now + timedelta(days=3650))
        .add_extension(x509.BasicConstraints(ca=True, path_length=None), critical=True)
        .add_extension(
            x509.KeyUsage(
                digital_signature=False,
                content_commitment=False,
                key_encipherment=False,
                data_encipherment=False,
                key_agreement=False,
                key_cert_sign=True,
                crl_sign=True,
                encipher_only=False,
                decipher_only=False,
            ),
            critical=True,
        )
        .sign(private_key=ca_key, algorithm=hashes.SHA256())
    )
    # Generate server key
    server_key = rsa.generate_private_key(public_exponent=65537, key_size=2048)
    # Subject for server cert
    server_name = x509.Name([x509.NameAttribute(NameOID.COMMON_NAME, cn)])
    san_list = []
    for item in alt_hosts:
        item = item.strip()
        if not item:
            continue
        try:
            san_list.append(x509.IPAddress(ip_address(item)))
        except ValueError:
            san_list.append(x509.DNSName(item))
    server_cert = (
        x509.CertificateBuilder()
        .subject_name(server_name)
        .issuer_name(ca_cert.subject)
        .public_key(server_key.public_key())
        .serial_number(x509.random_serial_number())
        .not_valid_before(now - timedelta(minutes=1))
        .not_valid_after(now + timedelta(days=days))
        .add_extension(x509.SubjectAlternativeName(san_list), critical=False)
        .add_extension(x509.ExtendedKeyUsage([ExtendedKeyUsageOID.SERVER_AUTH]), critical=False)
        .add_extension(x509.BasicConstraints(ca=False, path_length=None), critical=True)
        .add_extension(
            x509.KeyUsage(
                digital_signature=True,
                content_commitment=False,
                key_encipherment=True,
                data_encipherment=False,
                key_agreement=False,
                key_cert_sign=False,
                crl_sign=False,
                encipher_only=False,
                decipher_only=False,
            ),
            critical=True,
        )
        .sign(private_key=ca_key, algorithm=hashes.SHA256())
    )
    # Write files
    os.makedirs(os.path.dirname(CAFILE), exist_ok=True)
    with open(CAFILE, "wb") as f:
        f.write(ca_cert.public_bytes(serialization.Encoding.PEM))
    with open(KEYFILE, "wb") as f:
        f.write(
            server_key.private_bytes(
                encoding=serialization.Encoding.PEM,
                format=serialization.PrivateFormat.TraditionalOpenSSL,
                encryption_algorithm=serialization.NoEncryption(),
            )
        )
    with open(CERTFILE, "wb") as f:
        f.write(server_cert.public_bytes(serialization.Encoding.PEM))

